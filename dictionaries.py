birthdays = {}

def getBirthdayAndName():
  name = input("Enter your friends name: ")
  date = input("Enter their birthday in the format MM/DD/YYYY: ")

  birthdays[name] = date

  print(f"{name}'s birthday has been added.")

def displayBirthdays():
  print("Friends birthdays")
  for name, date in birthdays.items():
    print(f"{name}: {date}")

def viewBirthday():
  name = input("Enter the name of the friends birthday you want to view: ")
  print(f"{name}'s birthday is on: {birthdays[name]}")


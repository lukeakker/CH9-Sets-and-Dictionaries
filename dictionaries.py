#Nested dictionaries

dict1 = {'a' : 10, 'b' : 20, 'c' : 30}
dict2 = {'b' : 15, 'c' : 5, 'd' : 50}

combined = {}
for name in dict1:
    if name in dict2:
      combined[name] = dict1[name] + dict2[name]
    else:
      combined[name] = dict1[name]

for name in dict2:
    if name not in combined:
      combined[name] = dict2[name]

print(combined)

